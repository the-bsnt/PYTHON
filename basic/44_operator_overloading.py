# * Operator Overloading

print(1 + 2)  # addition
print("one" + "two")  # concatenate
print([1, 2, 3] + [4, 5])  # merge list

# thus one operator + is used to do different tasks such as addition, concatenation, and merging list
# This is operator overloading


